function detectLanguage() {
  const saved = localStorage.getItem('suphuzi_lang');
  if (saved && translations[saved]) return saved;
  const browser = navigator.language.split('-')[0];
  return translations[browser] ? browser : 'en';
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  localStorage.setItem('suphuzi_lang', lang);
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  // Update active language button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('text-blue-400', btn.dataset.lang === lang);
    btn.classList.toggle('text-gray-400', btn.dataset.lang !== lang);
  });
}

document.addEventListener('DOMContentLoaded', () => setLanguage(detectLanguage()));
